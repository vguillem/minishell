#include "includes/libft.h"

void	ft_putnbr(int n)
{
	unsigned int r;

	if (n >= 0)
		r = n;
	else
	{
		r = n * -1;
		ft_putchar('-');
	}
	if (r != 0)
	{
		while (r > 9)
		{
			ft_putnbr(r / 10);
			r = r % 10;
		}
	}
	ft_putchar(r % 10 + '0');
}
